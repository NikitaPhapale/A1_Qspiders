import java.util.Scanner;
class PassAndFail 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter your marks out off 600:");
		float marks=sc.nextFloat();
		float perc=(marks/600)*100;
		System.out.println("Percentage:"+perc);
		System.out.println((perc<=100 && perc>=75)?("A"):((perc<=75 && perc>=60)?("B"):((perc<=60 && perc>=35)?("C"):("Fail"))));
	}
}
