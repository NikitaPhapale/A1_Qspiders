class AreaAndPerimeterOfRectangle 
{
	public static void main(String[] args) 
	{
		float width= 4.5f;
		float height= 7.9f;
		System.out.println("Width(m):"+width);
		System.out.println("Height(m):"+height);

		float area=width*height;
		System.out.println("Area of Rectangle:"+area+" m^2");

		float perimeter=2*(width+height);
		System.out.println("Perimeter of Rectangle:"+perimeter+" m");

		
	}
}
